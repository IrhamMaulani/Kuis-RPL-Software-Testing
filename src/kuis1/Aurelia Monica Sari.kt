2.	Berdasarkan dari website yang saya baca yaitu https://www.huzefril.com/, Unit test adalah
tes yang dilakukan lakukan untuk mengetes sebuah class atau unit terkecil code/program kita.
Tujuannya tentu saja untuk memastikan bahwa Input, Proses, dan Output dari fungsi-fungsi yang
ada di class kita tersebut sesuai dengan requirement. Integration test artinya testing untuk
mengecek integrasi antara banyak class atau unit yang terlibat dalam sebuah alur proses.
Tujuannya tentu saja untuk memastikan bahwa alur proses berjalan secara sempurna. Biasanya untuk
memastikan konfigurasi integrasi sudah sesuai dengan yang diharapkan atau tidak.
Dari sumber yang saya baca mengenai tujuan dilakukannya unit test dan integration test maka dapat
disimpulkan bahwa kedua test tersebut harus dilakukan, karena pada kedua test tersebut kita dapat
memeriksa perangkat lunak yang kita buat masih memiliki kesalahan atau tidak. Jika kedua test ini
tidak dilakukan maka kita tidak dapat mengetahui perangkat lunak yang kita buat sudah dapat berjalan
dengan baik atau tidak, dan kita dapat memperbaiki kesalahan ataupun bug yang terapat pada perangkat
lunak yang kitda buat.
3.	Berdasarkan dari sumber yang saya baca yaitu https://www.ekrut.com/, QA Engineer (quality assurance
engineer) kerap menjadi salah satu posisi yang penting di dalam sebuah perusahaan teknologi. Tugas profesi
ini adalah untuk melakukan tes yang bertujuan untuk mencari masalah atau kelemahan dari suatu software
sebelum diluncurkan ke pengguna.
Dari sumber lain yang saya dapatkan yaitu https://campus.quipper.com/, Software engineer merupakan salah
satu profesi di bidang teknologi informasi. Seorang software engineer bertugas melakukan analisa, membuat rekayasa,
menyusun spesifikasi, mengimplementasikan dan memvalidasi suatu rancangan sistem perangkat lunak untuk menjawab
suatu permasalahan.
Dan dari sumber yang saya baca yaitu https://glints.com/, software testing adalah sebuah metode yang dijalankan
perusahaan untuk memeriksa apakah aplikasi sudah sesuai dengan persyaratan yang diharapkan atau belum.
Tak hanya itu, software testing juga dilakukan untuk memastikan bahwa produk bebas dari cacat. Metode tersebut
melibatkan proses pemeriksaan komponen dalam sistem software menggunakan alat manual atau otomatis.
Berdasarkan dari beberapa sumber diatas, dapat saya simpulkan bahwa QA Engineer dan Software Engineer memiliki
tugas yang berbeda. QA Engineer bertugas mengidentifikasi, memisahkan, dan melacak bug selama proses uji.
Sedangkan Software Engineer bertugas melakukan analisa, membuat rekayasa, menyusun spesifikasi, mengimplementasikan
dan memvalidasi suatu rancangan sistem perangkat lunak.
4.	Dari sumber yangsaya dapat yaitu https://www.dictio.id/, Software Testing atau Pengujian perangkat lunak
merupakan suatu proses investigasi yang dilakukan untuk mendapatkan informasi mengenai kualitas dari suatu produk
atau layanan yang sedang diuji, atau lebih spesifiknya software testing adalah proses mengeksekusi suatu program
untuk menemukan bug (kesalahan atau cacat lainnya) dari perangkat lunak.
Dapat disimpulkan apabila perangkat lunak tidak dilakukan test maka terdapat bug dalam perangkat lunak tersebut.
Contohnya keamanan pada perangkat lunak dapat dipertanyakan apakah sudah benar benar aman atau tidak, lalu apakah
performa dari perangkat lunak sudah seperti yang diharapkan, apakah tampilan dari perangkat lunak dapat dipahami
oleh pengguna, dan masih banyak lagi.
5.	Dari sumber yang saya baca yaitu https://socs.binus.ac.id/, Manual Testing adalah tipe testing dari Software
Testing, dimana tester melakukan testing terhadap software secara manual tanpa alat/tools, cara ini adalah cara
traditional dalam menemukan bug pada software. Setiap software baru harus terlebih dahulu melalui proses Manual
Testing sebelum bisa dilakukan Automation Testing, walaupun effort yang dikeluarkan sangatlah besar tetapi Manual
Testing sangatlah berguna karena mampu mendeteksi bug yang tidak dilihat oleh Automated Testing. Sedangkan Automation
Testing adalah Teknik Software Testing, dengan membandingkan hasil yang diinginkan dengan hasil yang sebenarnya, dan
biasanya dilakukan dengan menggunakan script atau testing tool, dalam Automation Testing pekerjaan dilakukan secara
berulang-ulang sehingga tidak mungkin untuk dikerjakan secara manual.
Berdasarkan sumber yang saya baca. Testing secara manual juga perlu dilakukan karena mampu mendeteksi bug yang tidak
terlihat oleh testing secara otomatisasi, sehingga apabila kita tidak melakukan testing secara manual kermungkinan
terburuknya adalah terdapat bug pada perangkat lunak yang sudah dirilis.