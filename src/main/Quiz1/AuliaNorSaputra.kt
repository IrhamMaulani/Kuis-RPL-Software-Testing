1.	Software testing sangat penting karena tujuan utama dari pengujian perangkat lunak
sebenarnya sederhana yaitu untuk memastikan bahwa software yang dihasilkan sesuai dengan
kebutuhan (requirement) yang sebelumnya ditentukan. Sehingga, sebelum dapat digunakan
sebuah software perlu melakukan software testing. Menurut saya sendiri software testing
sangat penting untuk dilakukan karena sebelum software tersebut dapat digunakan diperlukan
pengecekan ulang agar semua fungsi yang ada di dalamnya bekerja sesuai dengan keinginan developer.
Sumber:https://se.ittelkom-pwt.ac.id/2017/10/25/pentingnya-melakukan-pengujian-perangkat-lunak-software-testing/
2.	Perbedaan unit test dan integration test terdapat pada tempat serta bagian pengujiannya
maka, menurut pendapat saya terkait pernyataan diatas, untuk unit test jika tidak dilakukan
maka ketika publishing akan terdapat error maupun bug atau kesalahan, boros biaya, developer
kurang paham code dibanding yang melakukan testing. Pada integration test agak sama namun
kesalahannya yaitu akan banyak error di bagian service-service pada luar sistem aplikasi
kita misalnya yang bekerjasama dengan REST api atau service thrift (service third party),
kedua hal tersebut bila tidak dilakukan maka mungkin akan mengakibatkan ketidakakuratan aplikasi.
Sumber: https://www.testim.io/blog/unit-test-vs-integration-test/
3.	Pendapat saya mengenai QA Engineer yaitu berdasarkan sumber yang saya baca seorang
Quality Assurance merupakan orang yang bertanggung jawab untuk memastikan software atau
aplikasi yang diciptakan oleh sebuah perusahaan dapat bekerja dengan baik. Selain itu,
seorang Quality Assurance juga bekerja untuk menjabarkan bagian mana dari sebuah
software yang perlu diperbaiki. QA juga dapat membantu dalam meminimalisir terjadinya
pengerjaan ulang, memotivasi anggota tim, meningkatkan kepercayaan konsumen, dan lain-lain.
Sumber: https://glints.com/id/lowongan/apa-itu-quality-assurance/#.YmZNwU7P1PY
4.	Menurut situs https://testfort.com/blog/business-logic-in-qa-and-its-role-in-the-development-of-perfect-software”
business logic pada software yaitu menentukan bagaimana data dapat ditampilkan, disimpan, dibuat, dan
diubah. menyediakan sistem untuk memandu bagaimana objek bisnis bekerja satu sama lain.

Pada situs https://www.toptal.com/designers/ux/user-testing-methods menjelaskan
bahwa pengujiann UI difokuskan pada antarmuka pengguna situs web agar dapat
dipastikan bahwa antarmuka sudah sesuai dengan yang diinginkan dan pengguna
dapat memahami penggunaan UI dan UX pada web tersebut. Jadi dalam testing UI
dan UX berfokus pada test pengalaman pengguna dan bagaimana kesan sebuah produk atau web terhadap pengguna.

Jadi dapat saya simpulkan apabila dalam testing mengabaikan satu maupun banyak
test tersebut maka akan memungkinkan terjadinya error seperti data tidak dapat
ditampilkan,disimpan,dibuat,maupun diubah. Selain itu, apabila pada UI dan UX
tidak dilakukan test maka pada saat produk tersebut dipublish maka pengguna
akan mendapati error maupun pengguna akan kesulitan memahami fungsi dari antarmuka produk tersebut.
Selain itu, apabila tidak melakukan test maka keamanan software tidak akan
terjamin karena apabila terdapat sebuah celah maka pembajakan sangat mungkin terjadi pada produk tersebut.
5.	Terdapat perbedaan besar pada manual testing dan testing otomatif.
Pada manual testing perlu melakukan pengecekan pada setiap baris code
yang tentunya sangat menguras waktu dan pada testing otomatis developer
hanya perlu menyiapkan tools dan software yang akan memeriksa secara
otomatis project yang ingin ditest dan tentunya lebih hemat waktu dan biaya.
Sumber:https://www.perfecto.io/blog/automated-testing-vs-manual-testing-vs-continuous-testing
