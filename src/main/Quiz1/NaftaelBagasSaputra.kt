// Jawaban:

// 1. Menurut saya wajib melakukan tahap testing karena sebelum di implementasikan harus di cek apakah sesuai dengan permintaan dan dapat berjalan dengan baik atau tidak dengan adanya tahap testing maka developer tidak perlu mengulang dari awal jika terjadi kesalahan yang lebih fatal.

// 2. Menurut saya jika tidak dilakukannya kedua testing baik unit maupun integration maka perangkat lunak yang kita bangun tidak maksimal karena tidak dilakukannya cek dan pengujian dan dampak yang diberikan pastinya perangkat lunak tidak maksimal dan akan banyak bug dan error pada pernagkat lunak.

// 3. QA engineer dan software engineer dalam melakukan testing sama-sama memastikan perangkat berjalan dengan baik. Software Engineer mengeksplorasi cara untuk memeriksa cara kerja aplikasi dan menemukan kemungkinan cacat. Berbagai metode dan metodologi pengujian digunakan oleh penguji untuk menguji produk, menemukan bug, dan memeriksa apakah telah diperbaiki.Fokus utama Penguji adalah menguji aplikasi dan menemukan semua kemungkinan bug yang dapat merusak aplikasi. Sementara di sisi lain, QA Engineer mengikuti serangkaian metode dan aktivitas yang dirancang untuk memastikan bahwa aplikasi perangkat lunak sesuai standar atau SRS (Spesifikasi Kebutuhan Perangkat Lunak) dan sesuai dengan semua spesifikasi. Ini adalah strategi yang direncanakan dari evaluasi proses pengujian yang ditujukan untuk hasil produk yang berkualitas. Insinyur QA mencari cara untuk mencegah kemungkinan bug dalam proses pengembangan perangkat lunak. QA melewati seluruh siklus hidup produk (SDLC) dan memimpin proses pemeliharaan perangkat lunak. Fokus utama mereka adalah Kontrol Kualitas dan untuk memastikan persyaratan terpenuhi dengan benar.

// 4. Menurut saya jika pengujian yang tidak diberikan bisa di kerjakan saat pengembangan selanjutnya dan tidak terlalu krusial, tidak mengapa untuk tidak melakukan testing tersebut. tetapi jika testing itu berguna untuk melakukan pengujian pada bug atau error atau keamanan maka sebaiknya testing tersebut yang didahulukan.

// 5. Menurut saya testing manual diperlukan saat kita ingin mengetahui apakah ada human error pada perangkat lunak yang kita bangun, bisa saja terjad dimana source code tidak mengalami bug/error tetapi output yang kita inginkan tidak sesuai. testing secara otomatis memang membantu kita dalam mengecek apakah ada ada logical/syntax error namun tidak dapat menguji human error.

