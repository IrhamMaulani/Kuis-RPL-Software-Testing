Exploratory Testing. Sangat membutuhkan pengetahuan seorang tester analitikal/logika skill, kreatifitas, dan intuisi.
Usability Testing. Area ini dibutuhkan untuk melakukan pengecekan user-friendly, efisien, ataupun kenyamanan untuk
software atau produk untuk end users.
Ad-hoc Testing. Skenario ini dilakukan tanpa persiapan atau tidak menuliskan test case, sehingga QA
secara acak melakukan tes pada fugnsi di sistem. Dimana tujuannya adalah untuk secara kreatif untuk “Merusak”
