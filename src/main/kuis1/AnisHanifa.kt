4.  Terdapat banyak test pada perangkat lunak, mulai dari test business logic, UI, UX, Performa, sampai keamanan. Namun tidak semuanya dapat dilakukan karena
    keterbatasan waktu dan mahalnya biaya yang harus dikeluarkan. Berikan pendapat kalian dampak apa yang terjadi jika pengujian diatas tidak diberikan pada
    perangkat lunak.
    Jawab :
    Menurut saya, dampak yang dapat terjadi jika tidak dilakukan berbagai pengujian pada perangkat lunak adalah software berkemungkinan tidak berjalan
    sesuai dengan harapan dan standar serta timbulnya error atau bug pada software. Sehingga sebaiknya jika terjadi keterbatasan waktu dan biaya, lebih baik
    mengutamakan beberapa test penting, seperti test pada UI dan keamanan, untuk dilakukan terlebih dulu.
    Referensi :
    https://se.ittelkom-pwt.ac.id/2019/02/20/software-testing-dalam-lingkup-software-engineering/
    https://se.ittelkom-pwt.ac.id/2017/10/25/pentingnya-melakukan-pengujian-perangkat-lunak-software-testing/

5.  Berikan pendapat kalian tentang testing perangkat lunak secara manual? Apakah di saat banyaknya testing secara otomatisasi dilakukan testing secara
    manual perlu dilakukan?
    Jawab :
    Menurut saya, testing perangkat lunak secara manual masih perlu dilakukan karena terdapat beberapa hal yang tidak dapat diperoleh dari testing secara
    otomatisasi. Contohnya seperti feedback dari pengguna atau tester, pengguna dapat memberikan tanggapan apabila software memerlukan sesuatu (seperti
    kurangnya fitur tertentu) dan juga dapat memberikan informasi apabila menemukan bug saat menggunakan software.
    Referensi :
    https://socs.binus.ac.id/2020/06/30/software-testing/
    https://performancelabus.com/manual-testing-in-software-development/
    
