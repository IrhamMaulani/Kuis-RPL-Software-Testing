2.  Unit test dan integration test merupakan test yang dilakukan pada tahap implementasi. berikan pendapat kalian jika dua test ini tidak dilakukan beserta
    dampak yang akan terjadi pada perangkat lunak.
    Jawab :
    Unit Test merupakan mekanisme testing yang ditujukan untuk codebase yang berjalan di internal aplikasi, sementara Integration Test merupakan mekanisme
    testing dimana dilakukannya running sistem untuk berhubungan dengan service yang ada di luar sistem aplikasi.
    Menurut saya, jika unit test dan integration test tidak dilakukan, maka sulit untuk mengetahui apakah software dapat berjalan dengan baik, apakah
    software telah berjalan sesuai dengan yang diharapkan, serta apakah kinerja software telah maksimal. Adapun dampak yang timbul seperti perfoma software
    yang tidak sesuai harapan sehingga memberikan kesan buruk untuk pengguna software, terdapat banyak bug maupun error, serta munculnya biaya yang lebih
    banyak untuk memperbaiki software.
    Referensi :
    https://www.labana.id/view/memahai-software-testing-unit-test-integration-test-apa-perlu-dan-dampaknya/2016/06/27/?fullview

3.  Berikan pendapat kalian tentang pekerjaan QA Engineer sedangkan disisi lain Software Engineer telah melakukan testing.
    Jawab :
    Menurut saya, Quality Assurance Engineer dan Software Engineer sama-sama bertugas menjaga kualitas suatu software namun di tingkat yang berbeda.
    Software Engineer hanya melakukan testing pada tahap pengembangan, sementara Quality Assurance Engineer mengawasi kualitas dan pengembangan software di
    berbagai tingkat. Quality Assurance Engineer memastikan software telah memenuhi standar dan berjalan sesuai dengan harapan sebelum dapat diterima oleh
    pengguna.
